// Operadores de atribuição assignment
let x;

// assignment
x = 1;
console.log(x);

// addition assignment
x += 2;
console.log(x);

// subtraction assingnment
x -= 1;
console.log(x);

// multiplication assignment
x *= 2;
console.log(x);

// division assignment
x /= 2;
console.log(x);

// remainder division
x %= 2;
console.log(x);

// exponetiation
x **= 2;
console.log(x);
